const express = require("express");
/////////////////////////////////////////////////////////////////////////////////////////////

const notificationControllers = require('../controllers/notificationController');


/////////////////////////////////////////////////////////////////////////////////////////////

const router = express.Router();

/////////////////////////////////////////////////////////////////////////////////////////////

router.route('/confirmorder')
    .post(notificationControllers.confirmOrder);
router.route('/changestatus')
    .post(notificationControllers.changeStatus);
router.route('/confirmaccount')
    .post(notificationControllers.confirmAccount);
router.route('/sendad')
    .post(notificationControllers.sendAd);

/////////////////////////////////////////////////////////////////////////////////////////////

module.exports = router;